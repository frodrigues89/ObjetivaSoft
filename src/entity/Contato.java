package entity;

public class Contato {

	private String nomeContato;

	private String cargoContato;

	private int codigoArea;

	private int telefoneContato;

	private String emailContato;

	private String observacoesContato;

	public String getNomeContato() {
		return nomeContato;
	}

	public void setNomeContato(String nomeContato) {
		this.nomeContato = nomeContato;
	}

	public String getCargoContato() {
		return cargoContato;
	}

	public void setCargoContato(String cargoContato) {
		this.cargoContato = cargoContato;
	}

	public int getCodigoArea() {
		return codigoArea;
	}

	public void setCodigoArea(int codigoArea) {
		this.codigoArea = codigoArea;
	}

	public int getTelefoneContato() {
		return telefoneContato;
	}

	public void setTelefoneContato(int telefoneContato) {
		this.telefoneContato = telefoneContato;
	}

	public String getEmailContato() {
		return emailContato;
	}

	public void setEmailContato(String emailContato) {
		this.emailContato = emailContato;
	}

	public String getObservacoesContato() {
		return observacoesContato;
	}

	public void setObservacoesContato(String observacoesContato) {
		this.observacoesContato = observacoesContato;
	}

}
